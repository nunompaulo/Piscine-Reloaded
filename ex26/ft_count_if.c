int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	c;
	int	r;

	i = -1;
	c = 0;
	while (tab[++i])
	{
		r = (*f)(tab[i]);
		if (r == 1)
			c += 1;
	}
	return (c);
}
